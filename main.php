    <div class="tile">
        <h2>Португалия</h2>
        <img src="img\Португалия.jpg"
             class="image"/>
        <div class="text">Сэндвич франсезинья в переводе означает «маленькая француженка». Между двумя
            кусками белого хлеба кладут
            мясо, ветчину, колбасу и оливки, а сверху все покрывают плавленым сыром. Подают франсезинью
            в тарелке с
            томатным соусом. Блюдо едят с картошкой фри и запивают пивом.
