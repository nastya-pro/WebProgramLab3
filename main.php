        <div class="text">Блюдо под названием молохея появилось в Египте, свое наименование оно получило
            благодаря используемым в
            нем листьям молохеи — такого горьковатого овоща. Рубленные листья варятся в бульоне,
            приправленном
            чесноком и кориандром, а затем готовый суп подают с куриным или кроличьим мясом. В некоторых
            вариациях
            эти виды мяса могут заменять на рыбу или баранину.
