<h1>Гастрономический туризм</h1>
<div class="content_div">
    <div class="text">Путешествие — передвижение по какой-либо территории или акватории с целью их изучения,
        а также с
        общеобразовательными, познавательными, спортивными и другими целями.
        Это так прекрасно,когда знакомишся с новыми людьми,видишь старинную архитектуру, редких животных,
        участвуешь в фестивалях и болеешь на стадионах. Но все это было бы не так прекрасно без еды.
        Самый быстрый способ перекусить - это, конечно же, уличная еда. Фастфуд всегда пользовался
        популярностью, потому что это быстро и удобно, вкусно и разнообразно.
        в каждой стране есть свои любимые традиционные блюда, как быстрая еда, так и ресторанного типа.
    </div>
